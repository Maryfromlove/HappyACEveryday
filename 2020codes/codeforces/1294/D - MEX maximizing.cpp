void solve(int kaseId = -1) {
    int q, x;

    cin >> q >> x;
    vector<int> a(x, 0);

    int mina = 0;
    int minp = 0;

    while (q--) {
        int y, ans;
        cin >> y;
        y %= x;
        a[y]++;
        while (a[minp] > mina) {
            minp++;
            if (minp == x) {
                mina++;
                minp = 0;
            }
        }


        ans = mina * x + minp;
        cout << ans << endl;
    }
}

